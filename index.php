							<header class="major">
								<div>
								<h4 id="q1">Qu'est-ce qu'un Repair Café ? Qui êtes-vous ?</h4>
								<p id="r1">Le Repair Café est un endroit où l'on peut venir réparer un objet gratuitement avec l'aide de bénévoles expert en la matière. C'est également un lieu de rencontre et d'échanges entre visiteurs et bénévoles. Notre Repair Café Schaerbeek a été créé en avril 2013 par un groupe de citoyens motivés et intéressés par le concept.
								</p>
							</div>
								<h4 id="q2">Qui peut venir au Repair Café ?</h4>
								<p id="r2">Tout le monde ! Le Repair Café est ouvert à tous, car en plus d'être un endroit où l'on répare, c'est aussi un lieu convivial où l'on peut rencontrer des personnes et apprendre et partager des connaissances.</p>
								<h4 id="q3">Quand et où a lieu le Repair Café Schaerbeek ?</h4>
								<p id="r3">Notre Repair Café a lieu tous les deuxièmes dimanches du mois, rue des coteaux, de 14 à 18h. Vu le succès de certains ateliers, les inscriptions sont susceptibles d'être clôturées assez tôt.
								Il y a aussi d'autres Repair Cafés à Bruxelles et en Belgique avec des jours d'ouverture différents.</p>
								<h4 id="q4">Que coûte une réparation ?</h4>
								<p id="r4">C'est totalement gratuit ! Les organisateurs et réparateurs du Repair Café sont tous bénévoles. Les quelques frais d'organisation et de gestion sont financés par les dons récoltés durant l'évènement.</p>
								<h4 id="q5">Peut-on vous soutenir ou vous aider ?</h4>
								<p id="r5">Tout aide est toujours la bienvenue pour organiser nos ateliers, consultez notre page de soutien pour voir comment vous pouvez nous aider.</p>
								<h4 id="q6">Quels types d'objets pouvez-vous réparer ?</h4>
								<p id="r6">Lors de nos sessions nous avons différents ateliers de réparation pour des objets comme les appareils électriques, les vêtements ou encore les vélos. Vous trouverez plus d'informations sur les différents ateliers sur notre page de conseils pour préparer votre visite.</p>
								<h4 id="q7">Est-ce que mon objet pourra être réparé ?</h4>
								<p id="r7">Il n'est pas possible de savoir à l'avance si un objet est réparable. Les causes d'une même panne pouvant être multiples, seul le bénévole qui vous accueillera sur place pourra, selon ses connaissances, évaluer si l'objet est réparable. Les bénévoles du Repair Café Schaerbeek ne sont pas toujours les mêmes d'une session à l'autre et chacun a son domaine d'expertise. Dans la pratique, une majorité d'objets sont en général réparés lors des sessions. Plus d'information sur les différents ateliers lors de nos session sur notre page de conseils pour préparer votre visite.</p>
								<h4 id="q8">Puis-je prendre rendez-vous avec un réparateur ?</h4>
								<p id="r8">Non, le Repair Café ouvre à 14h et les visiteurs sont invités aux tables de réparation dans leur ordre d'arrivée. Les files pouvant parfois êtres longues pour certains ateliers, il nous arrive d'arrêter les inscriptions plus tôt dans l'après-midi.</p>
								<h4 id="q9">Pouvez-vous effectuer une réparation à domicile ?</h4>
								<p id="r9">Le Repair Café ne prévoit pas cela. Tous les réparateurs du Repair Café sont des bénévoles et ne s'engagent que pour la session et ils ne sont également assurés que sur place. L'idée est de réparer ensemble, en équipe, avec les moyens du bord. Les visiteurs sont donc invités à amener leur objet lors d'une session.</p>
								<h4 id="q10">Puis-je amener plusieurs objets à faire réparer en même temps ?</h4>
								<p id="r10">Vu le succès du Repair Café, et pour ne pas pénaliser les autres visiteurs, nous n'inscrivons qu'un seul objet par personne à votre arrivée. Mais cela ne vous empêche pas, après votre passage à la table de réparation, de faire une nouvelle fois la file à l'inscription pour inscrire un autre objet.</p>
